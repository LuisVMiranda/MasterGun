export function normalizeLocale(locale) {
  return TEXT[locale] ? locale : "en";
}
export function t(locale, key, values = {}) {
  const table = TEXT[normalizeLocale(locale)];
  const template = table[key] ?? getModeTranslation(locale, key) ?? TEXT.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name) => values[name] ?? "");
}
export function tStat(locale, stat) {
  return t(locale, `stat.${getStatAlias(stat)}`);
}

function getStatAlias(stat) {
  const aliases = { assistants: "soldiers", assistantAmmo: "soldierTraining", soldierAmmo: "soldierTraining" };
  return aliases[stat] ?? stat;
}
